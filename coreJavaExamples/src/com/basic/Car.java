package com.basic;

public class Car {
	int price;
 int avgSpeed;
 int enginePower;
 String make;
 String name ;
 String type;


public Car(int price, int avgSpeed, int enginePower, String make, String name, String type) {
	super();
	this.price = price;
	this.avgSpeed = avgSpeed;
	this.enginePower = enginePower;
	this.make = make;
	this.name = name;
	this.type = type;
}

}